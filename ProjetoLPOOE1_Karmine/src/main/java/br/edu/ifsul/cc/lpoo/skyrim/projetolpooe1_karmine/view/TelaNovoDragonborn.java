    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {
        if (d == null) {
            d = new Dragonborn();
        }

        d.setNome(txtNome.getText());
        d.setRaca((Raca) cmbRaca.getSelectedItem());

        int nivel = Integer.parseInt(txtNivel.getText());
        d.setNivel(nivel);

        Companion selectedCompanion = (Companion) cmbCompanion.getSelectedItem();

        if (selectedCompanion != null) {
            selectedCompanion.setDragonborn(d);
            d.setCompanion(selectedCompanion);
        }

        try {
            if (selectedCompanion != null && selectedCompanion.getId() == null) {
                jpa.persist(selectedCompanion);
            }
            jpa.persist(d);

            dispose(); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar o Dragonborn e o Companion: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

