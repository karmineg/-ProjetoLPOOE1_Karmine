    public void listarCompanion() {
        cmbCompanion.removeAllItems();
        List<Companion> companions = jpa.getCompanion();
        cmbCompanion.addItem(null);

        for (Companion c : companions) {
            if (c.getDragonborn() == null) {
                cmbCompanion.addItem(c);
            }
        }
    }

