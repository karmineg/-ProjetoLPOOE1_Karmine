    private void listarDragonborn() {
        cmbDragonborn.removeAllItems();
        List<Dragonborn> dragonborn = jpa.getDragonborn();
        for (Dragonborn d : dragonborn) {
            cmbDragonborn.addItem(d);
        }
    }

