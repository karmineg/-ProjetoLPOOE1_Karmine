    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {
        if (m == null) {
            m = new Missoes();
        }
        m.setDragonborn((Dragonborn) cmbDragonborn.getSelectedItem());
        m.setNome(txtMissao.getText());
        m.setRecompensa(txtRecompensa.getText());
        m.setDescricao(txtDescricao.getText());

        try {
            jpa.persist(m);
            JOptionPane.showMessageDialog(this, "Missão adicionada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar o Dragonborn " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {
        dispose();
    }

